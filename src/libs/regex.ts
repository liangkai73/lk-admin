/**
 * @desc: 常用正则表达式.
 */

export const regex = {
  // 正整数.
  integer_u: /^(0|[1-9][0-9]*)$/,

  // 整数.
  integer: /^(0|(\-)?[1-9][0-9]*)$/,

  // 正浮点数.
  float_u: /^(((0(\.\d*)?)|([1-9]\d*(\.\d*)?)))$/,

  // 浮点数.
  float: /^(\-?((0(\.\d*)?)|([1-9]\d*(\.\d*)?)))$/,

  // 中国的电话号码 (不带086开头).
  // mobile_cn: /^(((13[0-9])|(14[5|7])|(15([0-3]|[5-9]))|(18[0,5-9]))\d{8})$/,
  mobile_cn: /^(1[2-9][0-9]|14[579]|15[0-3,5-9]|16[6]|17[0135678]|18[0-9]|19[89]|98[0-9]|99[0-9])\d{8}$/,

  // email.
  email: /^(([A-Za-z0-9\u4e00-\u9fa5_-]|\.)+@[a-zA-Z0-9_-]+(\.[a-zA-Z0-9_-]+)+)$/,

  // 汉字.
  Chinese: /^[\u4e00-\u9fa5]{0,}$/,

  // 英文或数字集合.
  alphaOrDigit: /^[A-Za-z0-9]+$/,

  // 数字集合.
  digit: /^[0-9]+$/,

  // 日期: 2000-01-1
  date: /^\d{4}-\d{1,2}-\d{1,2}/
};
